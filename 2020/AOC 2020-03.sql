DECLARE @line		varchar(100)
		,@row		int	= 0
		,@column	int	= 0
		,@maxrow	int = 0
		,@maxcolumn	int = 0
		,@treecount	int = 0
		;

DECLARE [c] CURSOR LOCAL FORWARD_ONLY FAST_FORWARD READ_ONLY FOR
	SELECT	[value]
	FROM	STRING_SPLIT(REPLACE(@input, CHAR(13), ''), CHAR(10))

OPEN [c]

FETCH NEXT FROM [c] INTO @line;

WHILE @@FETCH_STATUS = 0
BEGIN
	IF LEN(@line) > 0
	BEGIN
		IF @maxcolumn = 0
			SET @maxcolumn = LEN(@line)

		SELECT @row += 1, @column = -1;

		WHILE 0 < @column OR @column = -1
		BEGIN
			SELECT @column = CHARINDEX('#', @line, @column+1)

			IF @column > 0
				INSERT INTO [#Tree]
					VALUES (@row, @column);
		END
	END

	FETCH NEXT FROM [c] INTO @line;
END

CLOSE [c];
DEALLOCATE [c];

SELECT @maxrow = @row;

EXEC [dbo].[#TreeCount] @rowOffset=1, @columnOffset=3, @maxrow=@maxrow, @maxcolumn=@maxcolumn, @treecount=@treecount OUTPUT;

