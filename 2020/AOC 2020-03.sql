DROP TABLE IF EXISTS [#Tree];
DROP PROCEDURE IF EXISTS [#TreeCount];

CREATE TABLE [#Tree]
(
	[row]		int
	,[column]	int
)
GO

CREATE PROCEDURE [#TreeCount](
	@rowOffset		int
	,@columnOffset	int
	,@maxrow		int
	,@maxcolumn		int
	,@treecount		int	OUTPUT
)
AS
BEGIN
	DECLARE	@row		int = 1
			,@column	int = 1
			;

	SELECT @treecount = 0;

	WHILE @row <= @maxrow
	BEGIN
		IF EXISTS (SELECT 1 FROM [#Tree] WHERE [row] = @row AND [column] = @column)
			SELECT	@treecount += 1;

		SELECT @row += @rowOffset, @column += @columnOffset;

		IF @column > @maxcolumn
			SELECT @column -= @maxcolumn;
	END

	RETURN @treecount;
END
GO


